package br.com.dto.generalInformation;

public class GeneralInformation {
	
	private Integer idInformation;
	private String websiteInformation;
	private String privacyPolicy;
	private String contact;
	private String aboutUs;
	private String siteNavigation;
	private String websiteImagePath;
	
	public GeneralInformation() {
		
	}

	public Integer getIdInformation() {
		return idInformation;
	}

	public void setIdInformation(Integer idInformation) {
		this.idInformation = idInformation;
	}

	public String getWebsiteInformation() {
		return websiteInformation;
	}

	public void setWebsiteInformation(String websiteInformation) {
		this.websiteInformation = websiteInformation;
	}

	public String getPrivacyPolicy() {
		return privacyPolicy;
	}

	public void setPrivacyPolicy(String privacyPolicy) {
		this.privacyPolicy = privacyPolicy;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getAboutUs() {
		return aboutUs;
	}

	public void setAboutUs(String aboutUs) {
		this.aboutUs = aboutUs;
	}

	public String getSiteNavigation() {
		return siteNavigation;
	}

	public void setSiteNavigation(String siteNavigation) {
		this.siteNavigation = siteNavigation;
	}

	public String getWebsiteImagePath() {
		return websiteImagePath;
	}

	public void setWebsiteImagePath(String websiteImagePath) {
		this.websiteImagePath = websiteImagePath;
	}

	
	
}
